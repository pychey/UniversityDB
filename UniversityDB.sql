-- =====================================================
-- 1. DATABASE CREATION AND STRUCTURE
-- =====================================================

-- Create the university database
CREATE DATABASE UniversityDB;
USE UniversityDB;

-- 1. Departments Table
CREATE TABLE Departments (
    department_id INT PRIMARY KEY AUTO_INCREMENT,
    department_name VARCHAR(100) NOT NULL UNIQUE,
    department_head VARCHAR(100),
    building VARCHAR(50),
    budget DECIMAL(12,2),
    established_year YEAR,
    phone VARCHAR(15),
    email VARCHAR(100)
);

-- 2. Faculty Table
CREATE TABLE Faculty (
    faculty_id INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    phone VARCHAR(15),
    department_id INT,
    position VARCHAR(50),
    salary DECIMAL(10,2),
    hire_date DATE,
    office_number VARCHAR(20),
    FOREIGN KEY (department_id) REFERENCES Departments(department_id)
);

-- 3. Students Table
CREATE TABLE Students (
    student_id INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    phone VARCHAR(15),
    date_of_birth DATE,
    admission_date DATE,
    graduation_date DATE,
    major_department_id INT,
    gpa DECIMAL(3,2),
    status ENUM('Active', 'Graduated', 'Suspended', 'Withdrawn'),
    FOREIGN KEY (major_department_id) REFERENCES Departments(department_id)
);

-- 4. Courses Table
CREATE TABLE Courses (
    course_id INT PRIMARY KEY AUTO_INCREMENT,
    course_code VARCHAR(10) UNIQUE NOT NULL,
    course_name VARCHAR(150) NOT NULL,
    description TEXT,
    credits INT DEFAULT 3,
    department_id INT,
    prerequisites VARCHAR(200),
    level ENUM('Undergraduate', 'Graduate'),
    FOREIGN KEY (department_id) REFERENCES Departments(department_id)
);

-- 5. Enrollments Table
CREATE TABLE Enrollments (
    enrollment_id INT PRIMARY KEY AUTO_INCREMENT,
    student_id INT,
    course_id INT,
    faculty_id INT,
    semester VARCHAR(20),
    year YEAR,
    grade CHAR(2),
    enrollment_date DATE,
    status ENUM('Enrolled', 'Completed', 'Dropped', 'Withdrawn'),
    FOREIGN KEY (student_id) REFERENCES Students(student_id),
    FOREIGN KEY (course_id) REFERENCES Courses(course_id),
    FOREIGN KEY (faculty_id) REFERENCES Faculty(faculty_id),
    UNIQUE KEY unique_enrollment (student_id, course_id, semester, year)
);

-- 6. Library Table
CREATE TABLE `Library` (
    book_id INT PRIMARY KEY AUTO_INCREMENT,
    isbn VARCHAR(13) UNIQUE,
    title VARCHAR(200) NOT NULL,
    author VARCHAR(150),
    publisher VARCHAR(100),
    publication_year YEAR,
    category VARCHAR(50),
    copies_available INT DEFAULT 1,
    total_copies INT DEFAULT 1,
    location VARCHAR(50)
);

-- 7. Financial_Records Table (Bonus table)
CREATE TABLE Financial_Records (
    record_id INT PRIMARY KEY AUTO_INCREMENT,
    student_id INT,
    transaction_type ENUM('Tuition', 'Fee', 'Payment', 'Refund', 'Scholarship'),
    amount DECIMAL(10,2),
    transaction_date DATE,
    semester VARCHAR(20),
    year YEAR,
    description VARCHAR(200),
    status ENUM('Pending', 'Completed', 'Cancelled'),
    FOREIGN KEY (student_id) REFERENCES Students(student_id)
);

